package WordAnalyzer;

public enum WordSymbol {
    Const, Void, Int, Char, Double, Struct,
    If, Else, Switch, Case, Default, While, For, Do,
    Return, Break, Continue, Print, Scan,
    UnsignedInt, Identifier, StringLiteral, CharLiteral,
    Plus, Multi, Minus, Div, Assign, Greater, Less,
    GreaterOrEqual, LessOrEqual, NotEqual, Equal,
    LeftBrace, RightBrace, LeftParenthesis, RightParenthesis,
    Comma, Semicolon, EOF, Unknown
}
